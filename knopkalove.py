# Подключаем библиотеку для создания окон (tkinter)
import tkinter as tk
# Подключаем модуль для случайных чисел (чтобы кнопка появлялась в разных местах)
import random

# Создаем главное окно программы (пустое пока что)
app = tk.Tk()
app.title('Кнопколов')               # Даем окну заголовок вверху окна
app.attributes('-fullscreen', True)  # Разворачиваем на весь экран (можно закрыть кнопкой Alt+F4 или Esc)
app.configure(bg='black')            # Устанавливаем черный фон окна (как в космосе)

# Создаем кнопку, которую будем ловить
# master=app - говорим, что кнопка живет внутри нашего окна app
# text='Лови!!!' - надпись на кнопке
# font - шрифт: Impact, размер 15, жирный
# bg='turquoise' - цвет фона кнопки (бирюзовый)
# width и height - ширина и высота кнопки (в символах и строках)
btn = tk.Button(
    master=app,
    text='Лови!!!',
    font=('Impact', 15, 'bold'),
    bg='turquoise',
    width=15,
    height=2
)

# Размещаем кнопку в окне:
# x=250 - отступ от левого края (в пикселях)
# y=50 - отступ сверху (в пикселях)
# Пока кнопка стоит высоко, но скоро будет падать!
btn.place(x=250, y=50)

# Создаем текстовую надпись (Label) для показа очков
# Она зеленая (dark green) и находится в левом верхнем углу
lbl = tk.Label(
    master=app, 
    text="очки: 0, скорость: 0",         # Начальный текст
    font=('Helvetica', 16), # Шрифт обычный, размер 16
    bg='dark green'         # Фон зеленый
)
lbl.place(x=10, y=10)       # В самом углу окна (отступ 10 пикселей)

# Запускаем приложение (бесконечный цикл, ждет нажатий мыши)
# mainloop держит окно открытым, пока не закроем программу
app.mainloop()

